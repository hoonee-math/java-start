package variable;

public class Var7 {

	public static void main(String[] args){
		int a = 100;        // 정수
		double b = 10.5;    // 실수
		boolean c = true;   // 불리언
		char d = 'A';       // 문자 하나, 작은 따옴표로 감싸야 한다.
		String e = "Hello Java"; // 문자열, 문자열을 다루기 위한 특별한 타입(대문자로 시작, class와 관련) 큰 따옴표로 감싸야 한다.

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}
