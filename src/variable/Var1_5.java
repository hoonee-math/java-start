package variable;

public class Var1_5 {

	public static void main(String[] args) {
		System.out.println(10);

		int a;
		a = 10; // 변수 초기화: 선언한 변수에 처음으로(최초로) 값을 대입해서 저장하는 것을 변수 초기화라고 한다.
		System.out.println(a);

		int c=3,d=4; // 한번에 여려 번수를 선언할 수 있다. 여러 변수 선언과 초기화를 한번에 할 수 있다.
		System.out.println(c);
		System.out.println(d);

	}
}
